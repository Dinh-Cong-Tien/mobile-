fun main() {
    val x = 20
    val y = 5

    println("Phep nhan: $x * $y = ${x * y}")
    println("Phep chia: $x / $y = ${x / y}")
    println("Phep cong: $x + $y = ${x + y}")
    println("Phep tru: $x - $y = ${x - y}")

    println("x lon hon y: ${x > y}")
    println("x nho hon hoac bang y: ${x <= y}")
    println("x khac y: ${x != y}")
}
